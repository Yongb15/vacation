package Exam02;

public class Ex16 {
    public static void main(String[] args) {

        int num = 2;

        num *= 2;                   //num = num * 2;      // 4

        System.out.println(num);

        num *= 2;                   //num = num * 2;      // 8

        System.out.println(num);

        num += 2;                                         // 10

        System.out.println(num);

        num -= 4;                                         // 6

        System.out.println(num);

    }
}
